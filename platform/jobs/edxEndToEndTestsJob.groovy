                  testSuite: 'e2e',
                  worker: 'jenkins-worker',
                  trigger: 'merge',
                  branch: '*/master',
                  refspec: '+refs/heads/master:refs/remotes/origin/master',
                  description: 'Verify the quality of changes made to the end-to-end tests',
